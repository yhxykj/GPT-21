func resizeImage(_ image: UIImage, maxPixelSize: CGFloat) -> UIImage? {
    let originalSize = image.size
    let maxSize = CGSize(width: maxPixelSize, height: maxPixelSize)
    
    // 计算缩放比例
    let aspectRatio = min(maxSize.width / originalSize.width, maxSize.height / originalSize.height)
    let newSize = CGSize(width: originalSize.width * aspectRatio, height: originalSize.height * aspectRatio)
    
    // 绘制到新尺寸的画布上
    UIGraphicsBeginImageContextWithOptions(newSize, true, 1.0)
    image.draw(in: CGRect(origin: .zero, size: newSize))
    let resizedImage = UIGraphicsGetImageFromCurrentImageContext()
    UIGraphicsEndImageContext()
    
    return resizedImage
}
